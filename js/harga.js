function SetInput(kode_buku,sekarang_jawa,sekarang_luar,tahun_depan, tahun_sekarang,depan_jawa,depan_luar ){
	document.getElementById("kode_bukudepan").value = kode_buku;
	document.getElementById("kode_bukusekarang").value = kode_buku;
	document.getElementById("sekarang_jawa").value = sekarang_jawa;
	document.getElementById("sekarang_luar").value = sekarang_luar;
	document.getElementById("tahun_sekarang").value = tahun_sekarang;
	document.getElementById("tahun_depan").value = tahun_depan;
	document.getElementById("depan_jawa").value = depan_jawa;
	document.getElementById("depan_luar").value = depan_luar;
}