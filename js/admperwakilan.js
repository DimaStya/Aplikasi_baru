function SetInput(kode_admper,nama_nasional,nama_area,alamat_perwakilan,nama_admper,email,no_telp){
	document.getElementById("kode_admper").value = kode_admper;
	document.getElementById("nama_nasional").value = nama_nasional;
	document.getElementById("nama_area").value = nama_area;
	document.getElementById("alamat_perwakilan").value = alamat_perwakilan;
	document.getElementById("nama_admper").value = nama_admper;
	document.getElementById("email").value = email;
	document.getElementById("no_telp").value = no_telp;
	document.getElementById("upnama_admper").value = nama_admper;
	document.getElementById("upemail").value = email;
	document.getElementById("upno_telp").value = no_telp;
}