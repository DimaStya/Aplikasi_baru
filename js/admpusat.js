function SetInput(kode_admpusat,kode_nasional,nama_area,nama_admpusat,email,no_telp){
	document.getElementById("kode_admpusat").value = kode_admpusat;
	document.getElementById("kode_nasional").value = kode_nasional;
	document.getElementById("nama_area").value = nama_area;
	document.getElementById("nama_admpusat").value = nama_admpusat;
	document.getElementById("email").value = email;
	document.getElementById("no_telp").value = no_telp;
}